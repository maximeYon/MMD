function t = tm_1x15_to_1x21(N)
% function t = tm_1x15_to_1x21(n2)
%
% Convert a fully symmetric fourth-order tensor on Voigt-like 
% notation (1x15) to a fourth-order tensor with minor and major symmetry
% in Voigt-like notation (1x21). Here we must assume something, so we
% go by the assumption that the bulk variance is zero

N = N ./ repmat(sqrt([1 4 4 6 12 6 4 12 12 4 1 4 6 4 1]), size(N,1), 1);

t = [...
    [N(:,1)  N(:,11) N(:,15)] * sqrt(1) ...
    [N(:,4)  N(:,6)  N(:,13)] * sqrt(2) ...
    [N(:,2)  N(:,8)  N(:,14)] * sqrt(4) ...
    [N(:,5)  N(:,3)  N(:,12)] * sqrt(4) ...
    [N(:,7)  N(:,10) N(:,9)]  * sqrt(4) ...
    [N(:,13) N(:,6)  N(:,4)]  * sqrt(4) ...
    [N(:,9)  N(:,8)  N(:,5)]  * sqrt(8) ];    
